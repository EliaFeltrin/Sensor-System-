  MX_GPIO_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  //select the column
	  for(int i=0; i<4;i++){
		  HAL_GPIO_WritePin(GPIOC,column_shift[i], GPIO_PIN_SET);
		  HAL_Delay(5);
		  // read 4 rows
		  for(int j=0; j<4; j++){
			  //debouncing: wait for 5 cycle of pressed button before printing

			  if(HAL_GPIO_ReadPin(GPIOC, row_shift[j])==GPIO_PIN_RESET){
				  tempo_riga[j][i]++;
				  if(tempo_riga[j][i]==3){
					  print[j][i]=1;
				  }
			  }
			  else if(HAL_GPIO_ReadPin(GPIOC, row_shift[j])==GPIO_PIN_SET){
				  tempo_riga[j][i]=0; //exit the while loop
				  flag_matrix[j][i]=0; //stop pressing the button before 5 cycle
				  // the character will not be printed in this iteration but it can in the next one
			  }


			  //finish check the row j
			  //Print if 5 cycles done
			  if(print[j][i]==1 && flag_matrix[j][i]==0){
				  int length=snprintf(string,sizeof(string),"%c\r\n",matrix_button[j][i]);
				  HAL_UART_Transmit(&huart2, string,length,100);
				  print[j][i]=0;
				  flag_matrix[j][i]=1; //ho stampato, se alla prossima iterazione sto ancora premendo non devo ristampare

			  }

		  }

		  HAL_GPIO_WritePin(GPIOC,column_shift[i], GPIO_PIN_RESET);
		  HAL_Delay(5);
	  	  }
  	  }




    /* USER CODE END WHILE */

